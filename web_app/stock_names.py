def get_stock_name(symbol: str) -> str:
    """
    获取股票中文名称

    Args:
        symbol: 股票代码，如 "000001.SZSE"

    Returns:
        股票中文名称，如果找不到则返回代码
    """
    return STOCK_NAMES.get(symbol, symbol)


def format_stock_symbol(symbol: str) -> str:
    """
    格式化股票显示为中文名称(代码)

    Args:
        symbol: 股票代码，如 "000001.SZSE"

    Returns:
        格式化后的显示，如 "平安银行(000001.SZSE)"
    """
    name = get_stock_name(symbol)
    if name != symbol:  # 找到了中文名称
        return f"{name}({symbol})"
    else:  # 没找到中文名称，只显示代码
        return symbol
